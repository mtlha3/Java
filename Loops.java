class loop{
    public static void main(String a[]) {

//For Loop
        for (int i = 0; i < 5; i++){
            System.out.println("Hello World");
        }

//while loop

        int j = 1;

        while (j<=5){
            System.out.println("Hello");
            j++;
        }


        // Do while loop

        int z = 4;

        do
        {
            System.out.println("Hi");
            z++;
        }while(z<10);
    }
}